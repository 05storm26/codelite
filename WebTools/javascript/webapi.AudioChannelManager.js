
/**
 * @brief The AudioChannelManager interface of the AudioChannels API includes features for managing your device's audio channels, including setting what channel's volume to affect when the volume buttons are pressed inside a particular app.
 * @link https://developer.mozilla.org/en-US/docs/Web/API/AudioChannelManager
 */
function AudioChannelManager() {

	/**
	 * @brief The headphones read-only property of the AudioChannelManager interface returns a Boolean that indicates whether headphones are plugged into the device. A true value means that headphone are plugged in; false means they aren't.
	 * @link https://developer.mozilla.org/en-US/docs/Web/API/AudioChannelManager/headphones
	 */
	this.telephonySpeaker = '';

	/**
	 * @brief The volumeControlChannel property of the AudioChannelManager interface returns a DOMString that allows you to set which audio channel should have its volume changed when you press the device's hardware volume controls.
	 * @link https://developer.mozilla.org/en-US/docs/Web/API/AudioChannelManager/volumeControlChannel
	 */
	this.volumeControlChannel = '';

	/**
	 * @brief The headphones read-only property of the AudioChannelManager interface returns a Boolean that indicates whether headphones are plugged into the device. A true value means that headphone are plugged in; false means they aren't.
	 * @link https://developer.mozilla.org/en-US/docs/Web/API/AudioChannelManager/headphones
	 */
	this.udioChannelManager.headphones

	/**
	 * @brief The telephonySpeaker property of the AudioChannelManager interface returns a Boolean that allows you to set whether the &quot;telephony&quot; audio channel should be played out of the device's speakers.
	 * @link https://developer.mozilla.org/en-US/docs/Web/API/AudioChannelManager/telephonySpeaker
	 */
	this.udioChannelManager.telephonySpeaker

	/**
	 * @brief The volumeControlChannel property of the AudioChannelManager interface returns a DOMString that allows you to set which audio channel should have its volume changed when you press the device's hardware volume controls.
	 * @link https://developer.mozilla.org/en-US/docs/Web/API/AudioChannelManager/volumeControlChannel
	 */
	this.udioChannelManager.volumeControlChannel

}

